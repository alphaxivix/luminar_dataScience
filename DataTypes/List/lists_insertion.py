#inserting into a list
#   there are several ways to insert items into a list:
#           1. using insert()
#           2. using append()
#           3. using extend()

mylist = ["Abel","Alpha","India"]
print(mylist)
mylist.append("Kerala")
print(mylist)
mylist.insert(3, "Asia")
print(mylist)
mylist.extend(["Iam","alphaxivix",409,"Nice","To","Meet you"])
print(mylist)

