#Required parameters

def Addition(x, y):
    return x + y

def difference(x, y):
    return x - y

def multiplication(x, y):
    return x * y

def division(x, y):
    return x / y

print("Addition =",Addition( 10 , 12))
print("Difference =",difference( 10 , 4))
print("Multiplication =",multiplication( 10 , 12))
print("Division =",division( 10 , 2))