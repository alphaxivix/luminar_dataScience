#Relational operators are used to compare two numbers/values , the output produced is always either True or False

#for example
num1 = 10
num2 = 20

print(num1>num2) #the output will be : False

print(num2>num1) #the output will be : True

# ____________________________________________________
# # Relational Operators are:                         |
# ____________________________________________________|
# #     <         ==>       lesser than               |
# #     >         ==>       greater than              |
# #     <=        ==>       lesser than or equal to   |
# #     >=        ==>       greater than or equal to  |
# #     ==        ==>       equal to                  |
# #     !=        ==>       not equal to              |
# ____________________________________________________|